using ChessChallenge.API;
using System;
using static System.Math;
using static ChessChallenge.API.BitboardHelper;

public class MyBot : IChessBot {

    public long nodes = 0; // #DEBUG
    public int maxSearchTime, searchingDepth;

    public Timer timer;
    public Board board;

    Move nullMove, searchBestMove, rootBestMove;

    // Assuming the size of TtEntry is indeed 16 bytes, this table is precisely 256MiB.
    (
        ulong, // hash
        ushort, // moveRaw
        short, // score
        short, // depth 
        ushort // bound BOUND_EXACT=[1, 65535), BOUND_LOWER=65535, BOUND_UPPER=0
    )[] transpositionTable = new (ulong, ushort, short, short, ushort)[0x1000000];

    int[,,] history = new int[2, 7, 64];

    ulong[] packedData = {
        0x0000000000000000, 0x2a2e1711312e1702, 0x2222231b2d251a09, 0x19222d20302b1708,
        0x2a3338283d412612, 0x5f664e497972381a, 0x95a48370b9b36562, 0x0003000400070006,
        0xfffcfffe00040002, 0x3229021d271d2606, 0x26250e17231c260b, 0x1e21171d2d231807,
        0x2a3326294039250e, 0x646e3a478a763411, 0x9ec8634be2d00c1d, 0x0000000000000000,
        0x5b503e3d3e2a352a, 0x6d634e4c5f543c3b, 0x85795b556d564e3b, 0x949064607e695d4c,
        0x9f956f74886a6057, 0x898b948975638054, 0x80707b776d4e4847, 0x6e7e4f0069010000,
        0x32362926302f2b33, 0x3531292f322f3635, 0x3c3a2c2e3a41342d, 0x363e342a403b2a31,
