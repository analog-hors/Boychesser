using ChessChallenge.API;
using System;
using static System.Math;
using static ChessChallenge.API.BitboardHelper;

public class MyBot : IChessBot {

    public long nodes = 0; // #DEBUG
    public int maxSearchTime, searchingDepth;

    public Timer timer;
    public Board board;

    Move nullMove, searchBestMove, rootBestMove;

    // Assuming the size of TtEntry is indeed 16 bytes, this table is precisely 256MiB.
    (
        ulong, // hash
        ushort, // moveRaw
        short, // score
        short, // depth 
        ushort // bound BOUND_EXACT=[1, 65535), BOUND_LOWER=65535, BOUND_UPPER=0
    )[] transpositionTable = new (ulong, ushort, short, short, ushort)[0x1000000];

    int[,,] history = new int[2, 7, 64];

    ulong[] packedData = {
        0x0000000000000000, 0x2228140e29271302, 0x191b1c16241d150a, 0x151d251b28221409,
        0x23292d2130302311, 0x43453f3a524e331c, 0x6d73676083805356, 0x0002000300050004,
        0xfffdfffe00020001, 0x2720021a221b1e01, 0x1d1a0b151b172108, 0x181a151b251c1404,
        0x222b1e21322b1e08, 0x48522e3265502511, 0x79964f41ac960226, 0x0000000000000000,
        0x4842484434213e35, 0x554f52524b3e4742, 0x665e5e5658455343, 0x6f6d62605f535c51,
